s = "0123456789"
print(s[2:4]) #slice from 2 to 4 (4 not included)
print(s[5:9])
print(s[:6]) # everyth until 6
print(s[4:]) # 4 onwards
print(s[:]) # entire thing
print(s[3::2]) # starting from 3 until end in steps of 2
print(s[:5:2]) # 0 to 4 in steps of 2
print(s[1::2]) # odd numbers
print(s[::-1]) # reverse order
